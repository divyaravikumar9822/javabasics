package com.basic3;

public class ArithmeticOperator {
	public static void main(String args[])
	{
		int x=40;
		int y=20;
		int result;
		result=x+y;
		System.out.println("add="+result);
		result=x-y;
		System.out.println("sub="+result);
		result=x*y;
		System.out.println("mul="+result);
		result=x/y;
		System.out.println("div="+result);
	}

}
